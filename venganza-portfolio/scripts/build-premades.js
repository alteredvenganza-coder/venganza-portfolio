/**
 * Build script: reads markdown files from content/premades/
 * and generates src/data/premades.js for the React app.
 *
 * Runs automatically before vite build via npm scripts.
 * Decap CMS creates/edits these markdown files from /admin.
 */

import { readdir, readFile, writeFile, mkdir } from 'fs/promises';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { existsSync } from 'fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const CONTENT_DIR = join(__dirname, '..', 'content', 'premades');
const OUTPUT_FILE = join(__dirname, '..', 'src', 'data', 'premades.js');

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const frontmatter = {};
  match[1].split('\n').forEach(line => {
    const idx = line.indexOf(':');
    if (idx === -1) return;
    const key = line.slice(0, idx).trim();
    let value = line.slice(idx + 1).trim();
    // Remove quotes
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }
    // Parse booleans and numbers
    if (value === 'true') value = true;
    else if (value === 'false') value = false;
    else if (/^\d+$/.test(value)) value = parseInt(value, 10);
    frontmatter[key] = value;
  });
  return frontmatter;
}

async function main() {
  if (!existsSync(CONTENT_DIR)) {
    console.log('[build-premades] No content/premades/ directory found, using existing data.');
    return;
  }

  const files = (await readdir(CONTENT_DIR)).filter(f => f.endsWith('.md'));

  if (files.length === 0) {
    console.log('[build-premades] No premade markdown files found, using existing data.');
    return;
  }

  const premades = [];

  for (const file of files) {
    const content = await readFile(join(CONTENT_DIR, file), 'utf-8');
    const data = parseFrontmatter(content);

    if (!data.number) continue;

    premades.push({
      id: parseInt(data.number, 10) || premades.length + 1,
      number: String(data.number).padStart(3, '0'),
      title: data.title || `Premade #${String(data.number).padStart(3, '0')}`,
      imageUrl: data.image || `/premades/${String(data.number).padStart(3, '0')}.jpg`,
      instagramUrl: data.instagram_url || '',
      price: data.price || 200,
      available: data.available !== false,
    });
  }

  // Sort by number
  premades.sort((a, b) => a.id - b.id);

  const jsContent = `// Auto-generated by scripts/build-premades.js — DO NOT EDIT MANUALLY
// Managed via Decap CMS at /admin
export const PREMADE_PRICE = 200;

export const premades = ${JSON.stringify(premades, null, 2)};
`;

  await mkdir(dirname(OUTPUT_FILE), { recursive: true });
  await writeFile(OUTPUT_FILE, jsContent, 'utf-8');
  console.log(`[build-premades] Generated ${premades.length} premades → src/data/premades.js`);
}

main().catch(err => {
  console.error('[build-premades] Error:', err);
  process.exit(1);
});
